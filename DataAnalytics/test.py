import numpy as np
arr1 = np.array([10,10,10,10])
arr2 = np.array([0, 20, 0, 20])
arr3 = np.array([-10, 30, -10, 30])

print("Arr1 avg: ", np.mean(arr1), "Arr1 standard deviation: ", np.std(arr1))
print("Arr2 avg: ", np.mean(arr2), "Arr2 standard deviation: ", np.std(arr2))
print("Arr3 avg: ", np.mean(arr3), "Arr3 standard deviation: ", np.std(arr3))

